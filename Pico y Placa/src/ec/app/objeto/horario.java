package ec.app.objeto;

public class Horario {

	//variables
	private String fecha;
	private String hora;
	
	//getters & setters
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
}

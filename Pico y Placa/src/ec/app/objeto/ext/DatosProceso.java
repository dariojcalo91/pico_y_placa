package ec.app.objeto.ext;

import java.time.LocalTime;

public class DatosProceso {
	
	private int varDia;
	private int varPlaca;
	private LocalTime varTiempo;
	
	
	public int getVarDia() {
		return varDia;
	}
	public void setVarDia(int varDia) {
		this.varDia = varDia;
	}
	public int getVarPlaca() {
		return varPlaca;
	}
	public void setVarPlaca(int varPlaca) {
		this.varPlaca = varPlaca;
	}
	public LocalTime getVarTiempo() {
		return varTiempo;
	}
	public void setVarTiempo(LocalTime varTiempo) {
		this.varTiempo = varTiempo;
	}
}
